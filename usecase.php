		le bouche à oreille. Pour répondre aux challenges de la Caisse d’Epargne Rhône Alpes, Krowdis a mis à disposition de la banque une application mobile, qui, sous forme de jeu, propose à tous de devenir influenceur pour une marque, pour une association ou un club sportif. En donnant aux supporters la possibilité de créer, facilement, du contenu pour leur club préféré, la Caisse d’Epargne Rhône Alpes provoque un sentiment d’appartenance autour de sa marque. Une fois créées, les créations des fans sont partagées dans leurs propres communautés d’amis.
