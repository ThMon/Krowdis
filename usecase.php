		marque, très visuel, sur lequel figure un ami,
		partagé par celui-ci. Et non un contenu sponsorisé diffusé par une marque sur lequel figure une star ou un inconnu. Pour mener à bien cette campagne, la Caisse d’Epargne Rhône Alpes et la startup ont mis en place une opération sur 3 matchs de l’ASVEL à domicile. Les supporters se sont mis en scène selon trois thématiques successives : élection du meilleur supporter de l’équipe, un challenge entre les fans, le staff du club et le sponsor, puis une campagne pour exprimer ses impressions sur le match en direct.
